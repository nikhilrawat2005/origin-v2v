async function seed() {
  console.log(`Seeding ${seedOpportunities.length} opportunities into 'org_opportunities'...`);
  const batch = db.batch();
  for (const opp of seedOpportunities) {
    const ref = db.collection("org_opportunities").doc(opp.id);
    batch.set(ref, {
      ...opp,
      source: "seed-india-2026",
      postedByUid: "seed-admin",
      createdAt: new Date().toISOString(),
    }, { merge: true });
  }
  await batch.commit();
  console.log("Done! Check Firestore console -> org_opportunities collection.");
}

seed().catch((err) => {
  console.error("Seed failed:", err);
  process.exit(1);
});
